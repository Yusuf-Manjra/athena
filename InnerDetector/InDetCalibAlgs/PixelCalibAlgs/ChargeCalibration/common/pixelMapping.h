void 
pixelMapping(const std::string & geographicalID, int *hashID, int *bec, int *layer, int *phimod, int *etamod) {
  const std::string_view s{geographicalID};
  ibl::Position p;
  auto pName = std::find(ibl::names.begin(), ibl::names.end(), s);
  if (pName != ibl::names.end()){
    auto idx = std::distance(ibl::names.begin(), pName);
    p = ibl::values[idx];
  }
  *hashID = p.hashID;
  *bec = p.bec;
  *layer = p.layer;
  *phimod = p.phimod;
  *etamod = p.etamod;
  return;
}
